    {
        // 選択しているゲームでフィルタ
        if(Auth::check()) {
            $request->merge(['game_id' => Auth::user()->selected_game_id]);
        }else{
            $request->merge(['game_id' => session('selected_game_id')]);
        }
        $rates = $this->user_service->getRatesWithPaginateOrderByRank($request, 50);

        return view('rank.index',compact('rates'));
    }

